#!/usr/bin/env node
// Render the print/PDF edition (deck/print.html) to cross-router-report.pdf via Playwright's
// Chromium page.pdf(). print.html is client-rendered (charts are SVG built by JS on load), so
// we load it, wait for the render, then page.pdf() — capturing the RENDERED page as vector
// (SVG stays crisp/scalable). Build print.html first: `node deck/build-deck.mjs`.
//   node cross-router/deck/render-pdf.mjs
import { existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

import { chromium } from "playwright";

const here = dirname(fileURLToPath(import.meta.url));
const printHtml = join(here, "out", "print.html"); // generated by build-deck (gitignored deck/out/)
const outPdf = join(here, "out", "cross-router-report.pdf");

if (!existsSync(printHtml)) {
  console.error("render-pdf: deck/print.html not found — run `node deck/build-deck.mjs` first.");
  process.exit(1);
}

const browser = await chromium.launch();
try {
  const page = await browser.newPage();
  await page.goto(pathToFileURL(printHtml).href, { waitUntil: "networkidle" });
  // print.html builds #doc synchronously on load; wait for the last-appended cohort to exist.
  await page.waitForSelector("section.cohort", { timeout: 20000 });
  await page.pdf({
    path: outPdf,
    format: "A4",
    landscape: true,
    printBackground: true, // cards/bars/why/board have background colours
    // margins mirror print-shell.html's `@page` (used when the file is printed from a browser
    // instead; page.pdf ignores CSS @page unless preferCSSPageSize, so we set them here too).
    margin: { top: "11mm", bottom: "11mm", left: "13mm", right: "13mm" },
  });
  console.log(`wrote ${outPdf}`);
} finally {
  await browser.close();
}
